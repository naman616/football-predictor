def download_file(url: str, dest: Path, timeout: int = 60) -> bool:
    """Download a file with progress indication. Returns True on success."""
    try:
        resp = requests.get(url, timeout=timeout, stream=True)
        resp.raise_for_status()
        dest.parent.mkdir(parents=True, exist_ok=True)
        with open(dest, "wb") as f:
            for chunk in resp.iter_content(chunk_size=8192):
                f.write(chunk)
        logger.info(f"Downloaded {url} -> {dest}")
        return True
    except Exception as e:
        logger.warning(f"Failed to download {url}: {e}")
        return False


def load_international_results(force_download: bool = False) -> pd.DataFrame:
    """Load international football results, downloading if needed."""
    raw_path = RAW_DIR / "results.csv"
    processed_path = PROCESSED_DIR / "results.parquet"

    if processed_path.exists() and not force_download:
        return pd.read_parquet(processed_path)

    if not raw_path.exists() or force_download:
        success = download_file(RESULTS_URL, raw_path)
        if not success:
            logger.warning("Using embedded fallback data")
            return _get_fallback_results()

    df = pd.read_csv(raw_path, parse_dates=["date"])
    df = _clean_results(df)
    df.to_parquet(processed_path, index=False)
    return df


def _clean_results(df: pd.DataFrame) -> pd.DataFrame:
    """Clean and standardize results dataframe."""
    df = df.dropna(subset=["home_team", "away_team", "home_score", "away_score"])
    df["home_score"] = df["home_score"].astype(int)
    df["away_score"] = df["away_score"].astype(int)
    df["date"] = pd.to_datetime(df["date"])
    df["year"] = df["date"].dt.year

    # Normalize result
    conditions = [
        df["home_score"] > df["away_score"],
        df["home_score"] == df["away_score"],
        df["home_score"] < df["away_score"],
    ]
    df["result"] = np.select(conditions, [0, 1, 2], default=1)  # 0=H, 1=D, 2=A

    df = df.sort_values("date").reset_index(drop=True)
    return df


def load_player_data() -> pd.DataFrame:
    """Load player statistics database."""
    processed_path = PROCESSED_DIR / "players.parquet"
    if processed_path.exists():
        return pd.read_parquet(processed_path)

    # Load from embedded CSV
    embedded_path = Path(__file__).parent.parent.parent / "data" / "players.csv"
    if embedded_path.exists():
        df = pd.read_csv(embedded_path)
        df.to_parquet(processed_path, index=False)
        return df

    return _generate_player_data()


