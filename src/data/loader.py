def _get_fallback_results() -> pd.DataFrame:
    """Minimal fallback dataset when download fails."""
    import numpy as np
    np.random.seed(42)
    teams = [
        "Brazil", "Argentina", "France", "England", "Spain",
        "Germany", "Italy", "Netherlands", "Portugal", "Belgium",
    ]
    records = []
    base_date = pd.Timestamp("2010-01-01")
    for i in range(500):
        home = np.random.choice(teams)
        away = np.random.choice([t for t in teams if t != home])
        hs = np.random.poisson(1.5)
        as_ = np.random.poisson(1.2)
        records.append({
            "date": base_date + pd.Timedelta(days=i * 3),
            "home_team": home,
            "away_team": away,
            "home_score": hs,
            "away_score": as_,
            "tournament": "Friendly",
            "city": "London",
            "country": "England",
            "neutral": False,
        })
    df = pd.DataFrame(records)
    return _clean_results(df)
